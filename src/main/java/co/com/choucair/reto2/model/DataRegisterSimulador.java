package co.com.choucair.reto2.model;

public class DataRegisterSimulador {
    private String valor_vivienda ;
    private String valor_necesario;
    private String procentaje;
    private String años;
    private String año;
    private String mes;
    private String dia;


    public String getValor_vivienda() {
        return valor_vivienda;
    }

    public void setValor_vivienda(String valor_vivienda) {
        this.valor_vivienda = valor_vivienda;
    }

    public String getValor_necesario() {
        return valor_necesario;
    }

    public void setValor_necesario(String valor_necesario) {
        this.valor_necesario = valor_necesario;
    }

    public String getProcentaje() {
        return procentaje;
    }

    public void setProcentaje(String procentaje) {
        this.procentaje = procentaje;
    }

    public String getAños() {
        return años;
    }

    public void setAños(String años) {
        this.años = años;
    }

    public String getAño() {
        return año;
    }

    public void setAño(String año) {
        this.año = año;
    }

    public String getMes() {
        return mes;
    }

    public void setMes(String mes) {
        this.mes = mes;
    }

    public String getDia() {
        return dia;
    }

    public void setDia(String dia) {
        this.dia = dia;
    }


}
